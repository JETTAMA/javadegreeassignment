    private void acceptButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_acceptButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) newOrdersTable.getModel();
        int index = newOrdersTable.getSelectedRow();
        
        if (newOrdersTable.getSelectionModel().isSelectionEmpty()) {
            JOptionPane.showMessageDialog(null, "Please select a row");
        }else{
            String orderId = (String) model.getValueAt(index, 2);
            int confirm = JOptionPane.showConfirmDialog(this,"Confirm to accept Order: \n"+ orderId+"\n?",                                                                
                        "Accept Order", 
                        JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                vendor.changeOrderStatus(orderId,Order.OrderStatus.ACCEPTED );
                JOptionPane.showMessageDialog(this,"Order has been accepted.");
            }else{
                JOptionPane.showMessageDialog(this,"Accepting order cancelled.");
            }
            
        }

        newOrdersTable.setModel(Controller.populateVendorOrderTableModel(vendor, Order.OrderStatus.PENDING));
    }//GEN-LAST:event_acceptButtonActionPerformed

    private void cancelButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) newOrdersTable.getModel();
        int index = newOrdersTable.getSelectedRow();
        
        if (newOrdersTable.getSelectionModel().isSelectionEmpty()) {
            JOptionPane.showMessageDialog(null, "Please select a row");
        }else{
            String orderId = (String) model.getValueAt(index, 2);
            int confirm = JOptionPane.showConfirmDialog(this,"Confirm to cancel Order: \n"+ orderId+"\n?",                                                                
                        "Cancel Order", 
                        JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                vendor.changeOrderStatus(orderId,Order.OrderStatus.CANCELLED);
                JOptionPane.showMessageDialog(this,"Order has been cancelled.");
            }else{
                JOptionPane.showMessageDialog(this,"Cancelling order cancelled.");
            }
            
        }
        newOrdersTable.setModel(Controller.populateVendorOrderTableModel(vendor, Order.OrderStatus.PENDING));

    }//GEN-LAST:event_cancelButtonActionPerformed

