    private void enrouteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enrouteButtonActionPerformed
        try{
        String orderId = orderIdField.getText();
        DefaultTableModel model = (DefaultTableModel) acceptedOrdersTable.getModel();
        int index = acceptedOrdersTable.getSelectedRow();
        String orderStatus = String.valueOf(model.getValueAt(index, 5));
        
        if (orderId.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please select a row.");
        }else{
            if(!"COMPLETED".equals(orderStatus)){
                JOptionPane.showMessageDialog(this,"The order is not completed yet though.");
                acceptedOrdersTable.clearSelection();
                
            }else{
                int confirm = JOptionPane.showConfirmDialog(this,"Confirm to update Order: \n"+ orderId+"\n?",                                                                
                            "Update Order", 
                            JOptionPane.YES_NO_OPTION);
                if (confirm == JOptionPane.YES_OPTION) {
                    deliveryRunner.changeDeliveryStatus(orderId,Order.DeliveryStatus.EN_ROUTE);
                    JOptionPane.showMessageDialog(this, "Successfully changed order to EN_ROUTE.");
                }else{
                    JOptionPane.showMessageDialog(this,"Operation cancelled.");
                } 
                Controller controller = new Controller();
                List<Order.DeliveryStatus> statuses= new ArrayList();
                statuses.add(Order.DeliveryStatus.ASSIGNED);
                statuses.add(Order.DeliveryStatus.EN_ROUTE);
                acceptedOrdersTable.setModel(controller.populateDeliveryRunnerTableModel(deliveryRunner, statuses));

                orderIdField.setText("");
            }   

        }
        }catch(ArrayIndexOutOfBoundsException e){
            JOptionPane.showMessageDialog(this,"Please select a row.");

        }


    }//GEN-LAST:event_enrouteButtonActionPerformed


