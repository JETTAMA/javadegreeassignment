    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // Set the vendor's balance in the balanceField text field.
        balanceField.setText(String.valueOf(deliveryRunner.getWallet().getBalance()));

        setTable();
        double totalSum; // Initialize the total sum to 0
        int rowCount = taskHistoryTable.getRowCount();
        totalSum = rowCount*3;
        revenue2Field.setText(String.valueOf(totalSum));

        Date today = new Date();
        startDateChooser.setDate(today);
        endDateChooser.setDate(today);
        
    }//GEN-LAST:event_formWindowOpened
    private void setTable(){
    // Get the dates from the JDateChoosers
        Date startDate = startDateChooser.getDate();
        Date endDate = endDateChooser.getDate();

        // Check if both dates are selected
        if (startDate != null && endDate != null) {
            // Ensure the start date is before the end date
            if (!startDate.after(endDate)) {
                // Call the method in the Controller to get the filtered model

                DefaultTableModel filteredModel = Controller.getFilteredRunnerOrderTableModelByDate(
                    this.deliveryRunner, 
                    Arrays.asList(Order.DeliveryStatus.PENDING,Order.DeliveryStatus.ASSIGNED,Order.DeliveryStatus.EN_ROUTE,Order.DeliveryStatus.DELIVERED), 
                    startDate, 
                    endDate
                );

                // Set the new model on the completedOrdersTable
                taskHistoryTable.setModel(filteredModel);
                double totalSum; // Initialize the total sum to 0
                int rowCount = taskHistoryTable.getRowCount();
                totalSum = rowCount*3;
                revenue2Field.setText(String.valueOf(totalSum));

            } else {
                JOptionPane.showMessageDialog(this, "Start date must be before end date.", "Date Selection Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            // Prompt the user to select both dates
            JOptionPane.showMessageDialog(this, "Please select both a start and end date.", "Date Selection Error", JOptionPane.ERROR_MESSAGE);
        }   
        
        clearFields();
        
        int rowCount = taskHistoryTable.getRowCount();
        if(rowCount<1){
            revenue2Field.setText("");
            JOptionPane.showMessageDialog(this, "No tasks during that period");

        }
    }
    
    private void clearFields(){
        try{
            if(taskHistoryTable.getRowCount()==0){
                revenue2Field.setText("");
            }
        }catch(Exception e){
        }
    }

